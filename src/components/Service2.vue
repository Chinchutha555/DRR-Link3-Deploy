<script setup>
import { ref } from "vue";
import short from "../assets/images/short.png";
import medium from "../assets/images/medium.png";
import long from "../assets/images/long.png";

const heading = "Phase 1";
const subHeading = "ผลการศึกษา";

const items = [
  {
    id: 1,
    name: "ระยะสั้น",
    des: "Far far away, behind the word mountains, far from the countries Vokalia and Consonantia.",
    colorClass: "color-1",
    icon: "app-indicator",
    photo: short,
  },
  {
    id: 2,
    name: "ระยะกลาง",
    des: "Far far away, behind the word mountains, far from the countries Vokalia and Consonantia.",
    colorClass: "color-2",
    icon: "arrow-repeat",
    photo: medium,
  },
  {
    id: 3,
    name: "ระยะยาว",
    des: "Far far away, behind the word mountains, far from the countries Vokalia and Consonantia.",
    colorClass: "color-3",
    icon: "briefcase",
    photo: long,
  },
];

const total = items.length;

const showModal = ref(false);
const selectedImage = ref(null);
const scale = ref(1);

const openImage = (img) => {
  selectedImage.value = img;
  scale.value = 1;
  showModal.value = true;
};

const closeImage = () => {
  showModal.value = false;
  scale.value = 1;
};

const zoomIn = () => {
  if (scale.value < 5) scale.value += 0.25;
};

const zoomOut = () => {
  if (scale.value > 1) scale.value -= 0.25;
};

const resetZoom = () => {
  scale.value = 1;
};

const handleWheel = (e) => {
  if (e.deltaY < 0 && scale.value < 5) {
    scale.value += 0.1;
  } else if (e.deltaY > 0 && scale.value > 1) {
    scale.value -= 0.1;
  }
};
</script>

<template>
  <div class="untree_co-section bg-light">
    <div class="container">
      <div class="row justify-content-between">
        <!-- LEFT -->

        <div class="col-lg-5 order-lg-2 js-custom-dots">
          <div class="text-right" style="margin-bottom: 30px">
