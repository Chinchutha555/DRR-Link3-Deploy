                />
                <path d="M16 3a3 3 0 1 1-6 0 3 3 0 0 1 6 0z" />
              </svg>

              <!-- icon 2 -->
              <svg
                v-else-if="item.icon === 'arrow-repeat'"
                class="bi bi-arrow-repeat"
